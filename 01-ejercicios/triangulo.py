#Dado de los valores ingresados por el usuario (base, altura) calcular y mostrar en pantalla el área de un triángulo.

print("**********CALCULO DE AREA DE UN TRIANGULO**********")
print("Ingresa la base del triángulo:")
base = float(input())
print ("Ingresa la altura del triángulo:")
altura = float(input())

area = (base*altura)/2

print("Área:",area)
