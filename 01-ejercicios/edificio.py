# Calcular y mostrar en pantalla la longitud de la sombra de un edificio de 20 metros de altura cuando el ángulo que forman los rayos del 
# sol con el suelo es de 22°

print("**********CALCULO DE SOMBRA DE EDIFICIO**********")

cateto_opuesto_altura = 20

tangente = 0.4

resultado = cateto_opuesto_altura / tangente

print (f"Longitud de la sombra del edificio: {resultado}")